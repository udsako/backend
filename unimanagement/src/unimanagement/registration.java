    public class HashingUtils {

    public static String hashPassword(String password) throws NoSuchAlgorithmException {
        MessageDigest digest = MessageDigest.getInstance("SHA-256");
        byte[] encodedHash = digest.digest(password.getBytes(StandardCharsets.UTF_8));
        return Base64.getEncoder().encodeToString(encodedHash);
    }
}
  
    
     private String generateStaffID(String unit) {
        String prefix = switch (unit.toLowerCase()) {
        case "cafeteria" -> "CAF";
        case "security" -> "SEC";
        case "maintenance" -> "MAI";
        case "facility" -> "FAC";
        case "horticulture" -> "HOR";
        default -> "GEN";
    };

 String staffID;
    try (Connection conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/universitymanagement", "root", "Awele2006")) {

        PreparedStatement ps = conn.prepareStatement(
            "SELECT COUNT(*) FROM registration WHERE staffID = ?");

        do {
            // Generate a random 12-character alphanumeric string
            String randomPart = UUID.randomUUID().toString().replace("-", "")
                    .substring(0, 5).toUpperCase();
            staffID = prefix + randomPart;

            ps.setString(1, staffID);
            ResultSet rs = ps.executeQuery();
            rs.next();
            if (rs.getInt(1) == 0) break; // Unique ID found, exit loop
        } while (true);

    } catch (Exception e) {
        e.printStackTrace();
        staffID = null;
    }

    return staffID;

    }
    // Generate a random password (8 characters long)
public static String generateRandomPassword() {
    StringBuilder password = new StringBuilder();
    String chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*()";
    Random random = new Random();
    for (int i = 0; i < 8; i++) {  // 8 character password
        int index = random.nextInt(chars.length());
        password.append(chars.charAt(index));
    }
    return password.toString();
}
