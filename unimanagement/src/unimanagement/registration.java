    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select Passport Photograph");
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.addChoosableFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
            "Image files", "jpg", "jpeg", "png"));

    int result = fileChooser.showOpenDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {
        java.io.File imageFile = fileChooser.getSelectedFile();

        try {
            BufferedImage image = ImageIO.read(imageFile);

            // Resize image to fit the label size
            Image scaledImage = image.getScaledInstance(
                jLabel9.getWidth(),
                jLabel9.getHeight(),
                Image.SCALE_SMOOTH
            );

            jLabel9.setIcon(new ImageIcon(scaledImage));
            jLabel9.setText(""); // Remove "No Photo" text

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to load image: " + e.getMessage());
        }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    
