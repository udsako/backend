    private DPFPFeatureSet extractFingerPrintCharacteristic(
            DPFPSample sample, DPFPDataPurpose purpose) {
        DPFPFeatureExtraction extractor
                = DPFPGlobal.getFeatureExtractionFactory().createFeatureExtraction();
        try {
            return extractor.createFeatureSet(sample, purpose);
        } catch (DPFPImageQualityException e) {
            return null;
        }
    }

// ────────────────────────────
// 4.4 Process a Captured Sample
// ────────────────────────────
    private void FingerCaptureProcess(DPFPSample sample) {
        // 1) Extract features for enrollment
        DPFPFeatureSet features
                = extractFingerPrintCharacteristic(sample, DPFPDataPurpose.DATA_PURPOSE_ENROLLMENT);

        if (features != null) {
            try {
                // 2) Add features to enrollment handler
                enrollmentHandler.addFeatures(features);

                // 3) Convert sample → image and display
                Image img = createFingerprintImage(sample);
                displayFingerprintImage(img);
                displayFingerprintImage1(img);

//                jLabel22.setText("Fingerprint captured. Remove finger.");
//                jLabel36.setText("Fingerprint captured. Remove finger.");
            } catch (DPFPImageQualityException ex) {
                return; // image quality was insufficient
            } finally {
                // 4) Check if we have a valid template
                switch (enrollmentHandler.getTemplateStatus()) {
                    case DPFPTemplateStatus.TEMPLATE_STATUS_READY:
                        fingerTemplate = enrollmentHandler.getTemplate();
                        stopFingerprintCapture();
//                        jLabel22.setText("Fingerprint ready for DB.");
//                        jLabel36.setText("Fingerprint ready for DB.");

                        JOptionPane.showMessageDialog(
                                this,
                                "Fingerprint captured successfully!",
                                "Fingerprint",
                                JOptionPane.INFORMATION_MESSAGE
                        );
                        break;

                    case DPFPTemplateStatus.TEMPLATE_STATUS_FAILED:
                        enrollmentHandler.clear();
                        stopFingerprintCapture();
//                        jLabel22.setText("Capture failed. Try again.");
//                        jLabel36.setText("Capture failed. Try again.");
                        startFingerprintCapture();
                        break;
                }

            }
        }
    }

