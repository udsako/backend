    String username = jTextField3.getText().trim();
    String useremail = jTextField4.getText().trim();

    if (username.isEmpty() || useremail.isEmpty()) {
        JOptionPane.showMessageDialog(jDialog1, "Please fill both fields.");
        return;
    }

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cafeteria", "root", "Awele2006")) {

        // Step 1: Check if user exists
        PreparedStatement checkUser = conn.prepareStatement("SELECT * FROM registration WHERE name=? AND email=?");
        checkUser.setString(1, username);
        checkUser.setString(2, useremail);
        ResultSet rs = checkUser.executeQuery();

        if (!rs.next()) {
            JOptionPane.showMessageDialog(jDialog1, "Username and email do not match.");
            return;
        }

        // Step 2: Prompt for password generation or manual entry
        int choice = JOptionPane.showConfirmDialog(jDialog1, "Would you like the system to generate a new password?", "Password Choice", JOptionPane.YES_NO_OPTION);

        String newPassword;
        String resetMethod;

        if (choice == JOptionPane.YES_OPTION) {
            newPassword = generateRandomPassword();
            resetMethod = "generated";
        } else {
            // Show password input dialog with masking and show password checkbox
            JPasswordField passwordField = new JPasswordField(20);
            JCheckBox showPassword = new JCheckBox("Show Password");
            JPanel panel = new JPanel();
            panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
            panel.add(new JLabel("Enter new password:"));
            panel.add(passwordField);
            panel.add(showPassword);

            showPassword.addActionListener(e -> {
                if (showPassword.isSelected()) {
                    passwordField.setEchoChar((char) 0);  // Show characters
                } else {
                    passwordField.setEchoChar('*');        // Mask characters
                }
            });

            int option = JOptionPane.showConfirmDialog(
                    jDialog1,
                    panel,
                    "Reset Password",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.PLAIN_MESSAGE);

            if (option != JOptionPane.OK_OPTION) {
                JOptionPane.showMessageDialog(jDialog1, "Password reset cancelled.");
                return;
            }

            newPassword = new String(passwordField.getPassword()).trim();

            if (newPassword.isEmpty()) {
                JOptionPane.showMessageDialog(jDialog1, "Password cannot be empty.");
                return;
            }

            // ✅ Password strength validation
            if (!validatePasswordStrength(newPassword)) {
                JOptionPane.showMessageDialog(jDialog1,
                        "Password must be at least 8 characters long and include:\n" +
                                "- At least one uppercase letter\n" +
                                "- At least one lowercase letter\n" +
                                "- At least one digit\n" +
                                "- At least one special character (e.g., @#$%^&+=!)");
                return;
            }

            resetMethod = "manual";
        }

        // Step 3: Hash the password
        String hashedNewPassword = HashingUtils.hashPassword(newPassword);

        // Step 4: Check password reuse
        PreparedStatement historyCheck = conn.prepareStatement("SELECT * FROM password_history WHERE email=? AND password_hash=?");
        historyCheck.setString(1, useremail);
        historyCheck.setString(2, hashedNewPassword);
        ResultSet historyRs = historyCheck.executeQuery();
        if (historyRs.next()) {
            JOptionPane.showMessageDialog(jDialog1, "You cannot reuse a previously used password.");
            return;
        }

        // Step 5: Update password in registration table
        PreparedStatement update = conn.prepareStatement("UPDATE registration SET password=? WHERE email=?");
        update.setString(1, hashedNewPassword);
        update.setString(2, useremail);
        update.executeUpdate();

        // ---- UPDATE password_expiry to 90 days from now ----
        PreparedStatement updateExpiry = conn.prepareStatement("UPDATE registration SET password_expiry = ? WHERE email = ?");
        long millisIn90Days = 90L * 24 * 60 * 60 * 1000; // 90 days in milliseconds
        java.sql.Timestamp expiryTimestamp = new java.sql.Timestamp(System.currentTimeMillis() + millisIn90Days);
        updateExpiry.setTimestamp(1, expiryTimestamp);
        updateExpiry.setString(2, useremail);
        updateExpiry.executeUpdate();

        // Step 6: Log to password_history table
        PreparedStatement logHistory = conn.prepareStatement("INSERT INTO password_history(email, password_hash) VALUES(?, ?)");
        logHistory.setString(1, useremail);
        logHistory.setString(2, hashedNewPassword);
        logHistory.executeUpdate();

        // Step 7: Log to passwordresetlog table
        PreparedStatement logReset = conn.prepareStatement("INSERT INTO passwordresetlog(username, email, reset_time) VALUES(?, ?, ?)");
        logReset.setString(1, username);
        logReset.setString(2, useremail);
        logReset.setTimestamp(3, new Timestamp(System.currentTimeMillis())); 
        logReset.executeUpdate();

        // Step 8: Send email
        String subject = "Your Password Has Been Reset";
        String body = "Hi " + username + ",\n\nYour new password is: " + newPassword +
                "\n\nPlease keep it from unathorized users.\n\nRegards,\nAdmin Team";

        email.sendEmail(useremail, subject, body);
        JOptionPane.showMessageDialog(jDialog1, "Password reset successfully. Check your email.");
        jDialog1.dispose();

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(jDialog1, "Error: " + ex.getMessage());
    }
    
    

