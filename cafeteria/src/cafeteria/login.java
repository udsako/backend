private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {
    String enteredemail = jTextField2.getText();
    String enteredPassword = new String(jPasswordField1.getPassword());
    String userRole = "";

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cafeteria", "root", "Awele2006");

        String query = "SELECT role FROM registration WHERE email = ? AND password = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, enteredemail);
        pst.setString(2, enteredPassword);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            userRole = rs.getString("role");
            System.out.println("User role found: " + userRole);
            JOptionPane.showMessageDialog(this, "Login successful as " + userRole);

            switch (userRole) {
                case "Ticketing Staff":
                    System.out.println("Opening Orders window");
                    new orders().setVisible(true);
                    break;
                case "Kitchen Staff":
                    System.out.println("Opening Kitchen window");
                    new kitchen().setVisible(true);
                    break;
                case "Serving Staff":
                    System.out.println("Opening Serving window");
                    new serving().setVisible(true);
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Unauthorized role.");
                    con.close();
                    return;
            }

            con.close();
            this.dispose();

        } else {
            System.out.println("Invalid credentials");
            JOptionPane.showMessageDialog(this, "Invalid credentials.");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Login failed: " + e.getMessage());
    }


}
